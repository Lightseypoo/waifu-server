CREATE DATABASE IF NOT EXISTS Test;
USE Test;
CREATE TABLE IF NOT EXISTS FFR_Scores(
  ID INTEGER PRIMARY KEY NOT NULL,
  Player TEXT NOT NULL,
  Title TEXT NOT NULL,
  Artist TEXT NOT NULL,
  Creator TEXT NOT NULL,
  W0 INTEGER NOT NULL,
  W1 INTEGER NOT NULL,
  W2 INTEGER NOT NULL,
  W3 INTEGER NOT NULL,
  W4 INTEGER NOT NULL,
  W5 INTEGER NOT NULL,
  Equiv FLOAT NOT NULL,
  Raw FLOAT NOT NULL,
  Rate FLOAT NOT NULL,
  TimeAdded TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  TimeAchieved TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS Users(
    ID INTEGER PRIMARY KEY NOT NULL,
    NameOsu TEXT,
    NameEtterna TEXT,
    NameQuaver TEXT,
    NameFFR TEXT,
    Group SMALLINT NOT NULL DEFAULT 1,
    Regdate TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);